class Operations{

    func sum(_ a:Float, _ b:Float) -> Float{
        return a + b
    }

    func substract(_ a:Float, _ b:Float) -> Float{
        return a - b
    }

    func divide(_ a:Float, _ b:Float) -> Float{
        return a / b
    }

    func multiply(_ a:Float, _ b:Float) -> Float{
        return a * b
    }


}